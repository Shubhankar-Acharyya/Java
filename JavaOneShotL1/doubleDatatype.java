package JavaOneShotL1;

public class doubleDatatype {
    public static void main(String[] args) {
        /* 
        double x = 3.1415, y = 2.001;
        System.out.println(x+y);
        */
        double x = 5, y = 2; // double x = 2 means value of x is 2.0
        /* 
        double z = 5/2;
        System.out.println(z); //This will give output as 2 because 5/2 is an integer and then that int getting stored as double 
        */
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
